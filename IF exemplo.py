# Definindo a variável 'nome' com o valor "Lira"
nome = "Lira"

# Verificando se o valor da variável 'nome' é igual a "Felipe"
if nome == "Felipe":
    # Se a condição acima for verdadeira, essa linha será executada e imprimirá "Seu nome é Felipe"
    print(f"Seu nome é {nome}")

# Essa linha está fora do bloco 'if', então será executada independentemente da condição 'if'
print("Está fora do if")
